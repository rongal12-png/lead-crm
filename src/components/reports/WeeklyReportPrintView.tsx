function fmtCurrency(n: number | null | undefined, lang: NarrativeLang): string {
  if (n == null) return "—";
  const locale = lang === "he" ? "he-IL" : "en-US";
  return new Intl.NumberFormat(locale, { style: "currency", currency: "USD", maximumFractionDigits: 0 }).format(n);
}

function fmtDate(iso: string, lang: NarrativeLang): string {
  const date = new Date(iso);
  const locale = lang === "he" ? "he-IL" : "en-GB";
  return date.toLocaleDateString(locale, { day: "numeric", month: "short", year: "numeric", timeZone: "UTC" });
}

function fmtShortDate(iso: string, lang: NarrativeLang): string {
  const locale = lang === "he" ? "he-IL" : "en-GB";
  return new Date(iso).toLocaleDateString(locale, { day: "numeric", month: "short" });
}

function fmtDateTime(iso: string, lang: NarrativeLang): string {
  const locale = lang === "he" ? "he-IL" : "en-GB";
  return new Date(iso).toLocaleString(locale);
}

