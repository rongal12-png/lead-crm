const paragraphStyle: React.CSSProperties = {
  fontSize: 14.5,
  lineHeight: 1.7,
  color: "#334155",
  margin: 0,
};

function Section({
  title,
  children,
  variant,
  accent,
}: {
  title: string;
  children: React.ReactNode;
  variant?: "success" | "warning" | "info";
  accent?: boolean;
}) {
  const variantBg: Record<string, string> = {
    success: "#f0fdf4",
    warning: "#fffbeb",
    info: "#eef2ff",
  };
  const variantBorder: Record<string, string> = {
    success: "#bbf7d0",
    warning: "#fde68a",
    info: "#c7d2fe",
  };
  const variantTitleColor: Record<string, string> = {
    success: "#15803d",
    warning: "#b45309",
    info: "#4338ca",
  };

  return (
    <div
      className="avoid-break"
      style={{
        marginBottom: 22,
        background: variant ? variantBg[variant] : "white",
        border: variant ? `1px solid ${variantBorder[variant]}` : "1px solid #e5e7eb",
        borderRadius: 12,
        padding: "16px 18px",
      }}
    >
      <h3
        style={{
          fontSize: 13,
          fontWeight: 900,
          textTransform: "uppercase",
          letterSpacing: 1.2,
          color: variant ? variantTitleColor[variant] : accent ? "#4338ca" : "#475569",
          margin: "0 0 12px 0",
        }}
      >
        {title}
      </h3>
      {children}
    </div>
  );
}

function Kpi({
  label,
  value,
  hint,
  color,
  lang,
}: {
  label: string;
  value: string | number;
  hint?: string;
  color: string;
  lang: NarrativeLang;
}) {
  const display = typeof value === "number" ? value.toLocaleString(lang === "he" ? "he-IL" : "en-US") : value;
  return (
    <div
      style={{
        background: "white",
        border: "1px solid #e5e7eb",
        borderInlineStart: `3px solid ${color}`,
        borderRadius: 10,
        padding: "10px 12px",
        minHeight: 60,
      }}
    >
      <div style={{ fontSize: 10, fontWeight: 700, color: "#94a3b8", textTransform: "uppercase", letterSpacing: 0.8 }}>
        {label}
      </div>
      <div style={{ fontSize: 22, fontWeight: 900, color: "#0f172a", lineHeight: 1.1, marginTop: 4 }}>{display}</div>
      {hint && <div style={{ fontSize: 11, color: "#64748b", marginTop: 2 }}>{hint}</div>}
    </div>
  );
}

function BulletList({ items, color }: { items: string[]; color: string }) {
  return (
    <ul style={{ margin: 0, paddingInlineStart: 18, fontSize: 14, lineHeight: 1.7, color: "#334155" }}>
      {items.map((it, i) => (
        <li key={i} style={{ marginBottom: 4 }}>
          <span style={{ color, fontWeight: 700 }}>•</span> {it}
        </li>
      ))}
    </ul>
  );
}

function Table({
  headers,
  rows,
  dir,
}: {
  headers: string[];
  rows: string[][];
  dir: "ltr" | "rtl";
}) {
  return (
    <div style={{ overflow: "hidden", borderRadius: 8, border: "1px solid #e5e7eb" }}>
      <table style={{ width: "100%", borderCollapse: "collapse", fontSize: 12.5, direction: dir }}>
        <thead style={{ background: "#f8fafc" }}>
          <tr>
            {headers.map((h, i) => (
              <th
                key={i}
                style={{
                  textAlign: dir === "rtl" ? "right" : "left",
                  padding: "8px 10px",
                  fontWeight: 800,
                  color: "#475569",
                  fontSize: 11,
                  textTransform: "uppercase",
                  letterSpacing: 0.6,
                  borderBottom: "1px solid #e5e7eb",
                }}
              >
                {h}
              </th>
            ))}
          </tr>
        </thead>
        <tbody>
          {rows.map((row, ri) => (
            <tr key={ri} style={{ borderBottom: ri === rows.length - 1 ? "none" : "1px solid #f1f5f9" }}>
              {row.map((cell, ci) => (
                <td
                  key={ci}
                  style={{
                    padding: "8px 10px",
                    color: ci === 0 ? "#0f172a" : "#475569",
                    fontWeight: ci === 0 ? 600 : 500,
                    textAlign: dir === "rtl" ? "right" : "left",
                  }}
                >
                  {cell}
                </td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

